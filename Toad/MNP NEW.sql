SELECT PORTINGTIME,
       CREATETIME,
       DECODE (RECIPENTID,  1, 'D200',  2, 'D300',  3, 'D100') || MSISDN
          AS RECIPIENT_PERFEX,
       DECODE (RECIPENTID,  1, 'BAKCELL',  2, 'AZERFON',  3, 'AZERCELL')
          AS KUDA,
       DECODE (DONOROPERATORID,  1, 'BAKCELL',  2, 'AZERFON',  3, 'AZERCELL')
          AS OTKUDA,
       PORTINGENDTIME
  FROM med.ent_mnp
 WHERE MSISDN LIKE '%994502080718%'